    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int ltt, lta,ht,hb,pn,hrg,cibul,lacil,Dp;
        ltt=Integer.parseInt(luastt.getText());
        lta=Integer.parseInt(luasta.getText());
        hb=Integer.parseInt(hargab.getText());
        ht=Integer.parseInt(hargat.getText());
        hrg= ltt+hb+ht;
        pn= (int) (0.1*hrg);
        harga.setText(String.valueOf(hrg));
        ppn.setText(String.valueOf(pn));

        if("10000000".equals(harga.getText())){
            dp.setText("100000");
            lamac.setText("12");
        }
        else if(harga.getText() == "900000"){
            dp.setText("1000000");
            lamac.setText("12");
        }
        else {
            dp.setText("8000000");
            lamac.setText("12");
        
        }
        lacil= Integer.parseInt(lamac.getText());
        cibul=(int)(hrg+pn-Integer.parseInt(dp.getText()));
        cicilanb.setText(String.valueOf(cibul));
        
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

