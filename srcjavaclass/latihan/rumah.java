   private void tambahData() throws SQLException {
       String tipeRumah;
       if(rb1.isSelected()){
           tipeRumah= "Tipe-36";
       }else if(rb2.isSelected()){
           tipeRumah="Tipe-45";
       }else {
           tipeRumah="Tipe-54";
       }
        try {
            String sql="INSERT INTO rumah VALUE('"+namap.getText()+"',"+"'"+area.getSelectedItem()+"',"+"'"+tipeRumah+"',"+"'"+luasta.getText()+"',"+"'"+harga.getText()+"',"+"'"+lamac.getText()+"',"+"'"+cicilanb.getText()+"')";
            java.sql.Connection conn=(Connection)KoneksiRumah.configDB();
            java.sql.PreparedStatement pst=conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan Data Berhasil");
        } catch (Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());

        }
   }
